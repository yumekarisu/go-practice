package main 
import "fmt"

func main() {
	//This is String
	//String can be combined using the "+" sign
	fmt.Println("go" + "lang")

	//This is Integer
	fmt.Println("1+1=", 1+1)
	
	//This is floating point
	fmt.Println("7.0/3.0=", 7.0/3.0)

	//This is Boolean
	fmt.Println(true && false) //AND
	fmt.Println(true || false) //OR
	fmt.Println(!true) //opposite value
}
